// if statement in JS
// An if statement is used to make decisions in your code. it let you run a certain pieces of code based on webkit-animation-play-state: weather a condition is true or false.

// basic Structure
// You start with the keyword if(), followed by the condition in parentheses (like checking if a number is greater than another number).
// if the condition is true, the code inside the curly braces {} runs.
// you can also use else to provide an alternative action if the condition is false

const number = -2 // try 2

if (number > 0) {
  console.log("Positive Number")
} else {
  console.log("Negative Number")
}

// using else if to add more condition
const age = 12
if (age < 13) {
  console.log("Child")
} else if (age < 20) {
  console.log("teen")
} else {
  console.log("Adult")
}
